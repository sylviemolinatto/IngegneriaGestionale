	public List<Actor> getVertici(String genre){
		
		String sql = "SELECT DISTINCT a.id,a.first_name,a.last_name,a.gender "
				+ "FROM actors a, roles r, movies_genres mg "
				+ "WHERE a.id = r.actor_id AND r.movie_id=mg.movie_id AND mg.genre=?";
		List<Actor> result = new ArrayList<Actor>();
		Connection conn = DBConnect.getConnection();

		try {
			PreparedStatement st = conn.prepareStatement(sql);
			st.setString(1, genre);
			ResultSet res = st.executeQuery();
			while (res.next()) {

				Actor actor = new Actor(res.getInt("id"), res.getString("first_name"), res.getString("last_name"),
						res.getString("gender"));
				
				result.add(actor);
			}
			conn.close();
			return result;
			
		} catch (SQLException e) {
			e.printStackTrace();
			return null;
		}
	}
	
	
	public List<Edge> getArchi(String genre, Map<Integer,Actor> idMap){
		
		String sql = "SELECT r1.actor_id as a1, r2.actor_id as a2, COUNT(*) as peso "
				+ "FROM roles r1, roles r2, movies_genres mg "
				+ "WHERE r1.movie_id=mg.movie_id AND r2.movie_id=mg.movie_id AND mg.genre=? AND r1.actor_id>r2.actor_id "
				+ "GROUP BY r1.actor_id,r2.actor_id";
		List<Edge> result = new ArrayList<Edge>();
		Connection conn = DBConnect.getConnection();

		try {
			PreparedStatement st = conn.prepareStatement(sql);
			st.setString(1, genre);
			ResultSet res = st.executeQuery();
			while (res.next()) {

				Actor a1 = idMap.get(res.getInt("a1"));
				Actor a2 = idMap.get(res.getInt("a2"));
				int weight = res.getInt("peso");
				
				result.add(new Edge(a1,a2,weight));
			}
			conn.close();
			return result;
			
		} catch (SQLException e) {
			e.printStackTrace();
			return null;
		}
	}
	
	
	
	
	
	
